let currentFilter ="all";

const totalNumber = document.getElementById("totalnumbers");
const open = document.getElementById("opened");
const close = document.getElementById("closed");
const allCardsSection = document.querySelector
(".allcard");

function init(){
  renderJobs();
  updateCounts();
}


function updateCounts() {
  const openIssue = issue.filter(a =>a.status !=="LOW");
  const closedIssue = issue.filter(a =>a.status=== "LOW");
  const totalIssue = issue.length;


  const totalNumber = document.getElementById("totalnumbers");
  const allB = document.getElementById("all")
  const open = document.getElementById("opened");
  const close = document.getElementById("closed");
  const count = document.querySelector(".num")

  // if (allB) allB.innerText = allissue.length;
  // if (close) close.innerText = closedBtn.length;
  // if (open) open.innerText = openBtn.length;

  if(count){
    let score = 0;
    if (currentFilter === "all") score = totalIssue;
    else if (currentFilter === "opened") score = openIssue.length;
    else if (currentFilter === "closed") score = closedIssue.length;
    count.innerText = score;
  }
  
}

function renderJobs (){
  allCardsSection.innerHTML = "";


  


  const filteredIssue = issue.filter(issue => {
    if  (currentFilter === "all" ) return true;
    if (currentFilter === "opened") return issue.status !=="LOW";
    if (currentFilter === "closed") return issue.status === "LOW";
  });

  if(filteredIssue.length === 0){
    allCardsSection.innerHTML = `<div class="text-center py-20 text-gray-400">No issues found.</div>`;
    return;
  }
  filteredIssue.forEach(issue => {
      const section = document.createElement("section");

      let color ="red";
      let borderColor = "green"

  if(issue.status==="HIGH") {
    color = "red";
    borderColor = "green";
  }
  else if (issue.status==="MEDIUM") {
    color =  "yellow";
    borderColor= "green";
  }
  else if (issue.status==="LOW"){
    color ="gray";
    borderColor="purple";
  } 

  let icon=issue.icon;

  if (issue.status === "HIGH"){
    icon = "./B13-A5-Github-Issue-Tracker/assets/Open-Status.png";
  }
  else if (issue.status === "MEDIUM"){
    icon = "./B13-A5-Github-Issue-Tracker/assets/Open-Status.png";
  }
  else if (issue.status === "LOW"){
    icon = "./B13-A5-Github-Issue-Tracker/assets/Closed- Status .png";
  }



    section.className = "mb-10";
    let statusClass = "btn-soft btn-primary";
    if (issue.status === "OPENED") statusClass = "btn-success text-white";
    section.innerHTML =`
    <div class="w-70  border-t-5 border-${borderColor}-600 rounded-lg shadow-2xl sm:ml-20 lg:m-auto ">
      <div class="flex justify-between mt-4 mr-3 "><img class=" ml-5 h-8 w-8" src="${icon}">
        <p class="px-6 py-1  rounded-2xl text-${color}-600 bg-${color}-200 border-2  border-${color}-400">${issue.status}</p>

      </div>
      <div>
        <h2 class="font-bold text-xl px-5">${issue.title}</h2>
        <p class="text-gray-400 px-5">(${issue.description})</p>
      </div>
      <div class="px-5 py-4 border-b-2 border-gray-300">
        ${issue.labels.map(label => 
  `<button class="px-3.5 rounded-xl text-${label.color}-600 bg-${label.color}-200 border-2 border-${label.color}-400 mr-2">
     ${label.name}
   </button>`).join(' ')}
      </div>
      <div class="p-5  text-gray-500">
        <p class="mb-2">${issue.assignee}</p>
        <p>${issue.date}</p>
      </div>
    </div>
    `;
    allCardsSection.appendChild(section);
  });
}

function toggleStyle(id){
  const all = document.getElementById("all");
  const open = document.getElementById("opened");
  const close = document.getElementById("closed");


  if (id==="all") currentFilter ="all";
  else if ( id === "opened") currentFilter = "opened";
  else if ( id ==="closed") currentFilter = "closed";
  

  [all,open,close].forEach(btn =>
  {
    btn.classList.remove("bg-black","text-white");
    btn.classList.add("bg-gray-300","text-gray-500");
  })

  const selected = document.getElementById(id);
  selected.classList.remove("bg-gray-300","text-gray-500");
  selected.classList.add("bg-blue","text-white");
  
CSSContainerRule.innerHTML="";
  renderJobs();
  updateCounts();
}
